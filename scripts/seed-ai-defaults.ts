async function main() {
  const conn = await getConn();
  console.log("✓ DB connected");

  try {
    // ── Seed Personas ──
    console.log("\n[1/2] Seeding AI Agent Personas...");
    for (const p of PERSONAS) {
      await conn.execute(
        `INSERT INTO ai_agent_personas (id, name, description, system_prompt, tone, is_default, created_at, updated_at)
         VALUES (?, ?, ?, ?, ?, ?, NOW(), NOW())
         ON DUPLICATE KEY UPDATE
           name = VALUES(name),
           description = VALUES(description),
           system_prompt = VALUES(system_prompt),
           tone = VALUES(tone),
           is_default = VALUES(is_default),
           updated_at = NOW()`,
        [p.id, p.name, p.description ?? null, p.systemPrompt, p.tone, p.isDefault]
      );
      console.log(`  ✓ ${p.name} (tone=${p.tone}, default=${p.isDefault === 1})`);
    }

    // ── Seed Knowledge Base ──
    console.log("\n[2/2] Seeding Knowledge Base Documents...");
    for (const doc of KB_DOCS) {
      const docId = `kb-${doc.fileName.replace(/[^a-z0-9]/gi, "-").slice(0, 32)}`;
      const chunks = chunkText(doc.content);

      // Delete existing chunks for idempotency
      await conn.execute(`DELETE FROM rag_chunks WHERE document_id = ?`, [docId]);

      // Upsert document
      await conn.execute(
        `INSERT INTO rag_documents (id, title, file_name, char_count, chunk_count, uploaded_at)
         VALUES (?, ?, ?, ?, ?, NOW())
         ON DUPLICATE KEY UPDATE
           title = VALUES(title),
           char_count = VALUES(char_count),
           chunk_count = VALUES(chunk_count),
           uploaded_at = NOW()`,
        [docId, doc.title, doc.fileName, doc.content.length, chunks.length]
      );

      // Insert chunks
      for (let i = 0; i < chunks.length; i++) {
        const chunkId = `${docId}-c${String(i).padStart(3, "0")}`;
        await conn.execute(
          `INSERT INTO rag_chunks (id, document_id, chunk_index, content) VALUES (?, ?, ?, ?)`,
          [chunkId, docId, i, chunks[i]]
        );
      }

      console.log(`  ✓ "${doc.title}" → ${chunks.length} chunks`);
    }

    console.log("\n✅ Seed selesai!");
    console.log("\nPersona:");
    PERSONAS.forEach((p) =>
      console.log(`  - ${p.name} (tone=${p.tone}, default=${p.isDefault === 1})`)
    );
    console.log("\nKnowledge Base:");
    KB_DOCS.forEach((d) => console.log(`  - ${d.title}`));
  } finally {
    await conn.end();
  }
}

main().catch((e) => {
  console.error("Seed gagal:", e);
  process.exit(1);
});
